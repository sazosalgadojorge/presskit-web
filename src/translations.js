const STORAGE_KEY = 'presskit-locale'

/**
 * Detecta idioma preferido: primero localStorage, luego navegador.
 * Acepta 'es' o cualquier código que empiece por 'es' (ej. es-CL, es-MX).
 */
export function getInitialLocale() {
  if (typeof window === 'undefined') return 'es'
  const stored = localStorage.getItem(STORAGE_KEY)
  if (stored === 'es' || stored === 'en') return stored
  const browser = navigator.language || (navigator.languages && navigator.languages[0]) || 'en'
  return browser.toLowerCase().startsWith('es') ? 'es' : 'en'
}

export function setStoredLocale(locale) {
  if (typeof window === 'undefined') return
  localStorage.setItem(STORAGE_KEY, locale)
}
